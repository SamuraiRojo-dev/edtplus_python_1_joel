# Operadores de coparacion
# Menor que (<) mayor que (>)

print(2 > 1) # True
print(1 > 2) # False
print(10 > 10) # False

print(1 < 2) # True
print(10 < 5) # False
print(10 < 10) # False

# Mayor o igual(>=)

print(10 >= 10) # True
print(20 >= 10) # True

# Menor o igual(<=)
print(9 <= 0) # False
print(7 <= 10) # True

# Igualdad (==)
print(10 == 10) # True
print(7 == 10) # False
print(10 == "10") # False
print("Hola" == "Hola") # True
print("Hola" == "hola") # False

# Desigualdad (!=)
print(1 != 1) # False
print(1 != "1") # True
print("Hola" != "Hola") # False
print("Hola" != "hola") # True

